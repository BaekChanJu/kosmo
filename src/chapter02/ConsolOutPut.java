package chapter02;

public class ConsolOutPut {
	public static void main(String[] args) {
		System.out.println("안녕"); //println : 프린트 후 엔터 라는 뜻
		System.out.println("하세요");//엔터가 들어가니 두줄로 나옴
		
		System.out.print("안녕 "); //print : 그냥출력 그러니 그냥 한줄로나옴
		System.out.print("하세요 ");
		
		System.out.println(); // 비워두면 그냥 엔터 의미
		
		System.out.println("안녕 \n "); // \n : 엔터의미
		System.out.println("하세요. ");
	}

}
